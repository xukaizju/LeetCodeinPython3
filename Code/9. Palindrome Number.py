class Solution(object):
    def isPalindrome(self, x):
        if x>=0:
            y=x
        else:
            return 1==2
        n=len(str(y))
        a=0
        b=n-1
        while a<=b:
            if str(y)[a]==str(y)[b]:
                a+=1
                b-=1
            else:
                return 1==2
        return 1==1
